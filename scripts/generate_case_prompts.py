ENDING = "输出一张编辑后的灰度医学图像，不要输出文字。"


def build_prompt(modality: str, method: str) -> str:
    parts = [
        COMMON_PREFIX,
        MODALITY_ANATOMY[modality],
        METHOD_STYLE[method],
        MODALITY_TWEAKS[modality],
    ]
    extra = MODALITY_METHOD_EXTRA.get((modality, method))
    if extra:
        parts.append(extra)
    parts.append(ENDING)
    return "".join(parts)


def main() -> None:
    payload = {
        "modalities": MODALITIES,
        "methods": METHODS,
        "prompts": {
            modality: {method: build_prompt(modality, method) for method in METHODS}
            for modality in MODALITIES
        },
    }
    output = Path("configs/nanobanana_prompts_case_current_v2.json")
    output.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")
    print(output)


if __name__ == "__main__":
    main()
